def extract_svgs_from_yaml():
    """Extract SVGs from hover-definitions.yaml, deduplicating by visual content."""
    path = REPO / 'build' / 'hover-definitions.yaml'
    data = yaml.safe_load(path.read_text())

    entries = []
    seen_titles = set()

    for key, val in data.items():
        if not isinstance(val, dict):
            continue
        html = val.get('html', '')
        if '<svg' not in html:
            continue

        svg_match = re.search(r'(<svg[\s\S]*?</svg>)', html)
        if not svg_match:
            continue

        svg = svg_match.group(1)
        title_match = re.search(r'<title>(.*?)</title>', svg)
        title_text = title_match.group(1) if title_match else ''

        if title_text in seen_titles and title_text:
            for e in entries:
                if e['title'] == title_text:
                    e['terms'].append(key)
                    break
            continue

        if title_text:
            seen_titles.add(title_text)

        text_before = re.search(r'^(.*?)<svg', html, re.DOTALL)
        context_html = text_before.group(1).strip() if text_before else ''

        hover_id = val.get('hover_id', '')
        target = val.get('target', '')

        entries.append({
            'key': key,
            'terms': [key],
            'svg': svg,
            'title': title_text,
            'context_html': context_html,
            'hover_id': hover_id,
            'target': target,
        })

    return entries


def extract_svgs_from_preprocess():
    """Extract the inline SVGs generated by preprocess.py functions."""
    path = REPO / 'build' / 'preprocess.py'
    sys.path.insert(0, str(REPO / 'build'))

    source = path.read_text()

    flat_match = re.search(r"FLAT_SVG\s*=\s*'''(.*?)'''", source, re.DOTALL)
    flat_svg = ''
    if flat_match:
        flat_html = flat_match.group(1)
        svg_m = re.search(r'(<svg[\s\S]*?</svg>)', flat_html)
        if svg_m:
            flat_svg = svg_m.group(1)

    domain_match = re.search(r"DOMAIN_SVG\s*=\s*'''(.*?)'''", source, re.DOTALL)
    domain_svg = ''
    if domain_match:
        domain_html = domain_match.group(1)
        svg_m = re.search(r'(<svg[\s\S]*?</svg>)', domain_html)
        if svg_m:
            domain_svg = svg_m.group(1)

    # For the filmstrip, we need to run the code that generates panels.
    # Import and execute just the panel generation logic.
    filmstrip_panels = generate_filmstrip_panels()

    genesis_svgs = {}
    for varname, label in [
        ('AUTOCATALYTIC_LOOP', 'Autocatalytic loop'),
        ('EDGE_OF_CHAOS', 'Edge of chaos'),
        ('SUBSTRATE_PARALLEL', 'Substrate parallel'),
        ('CANOPY_PROBLEM', 'Canopy problem'),
    ]:
        m = re.search(rf"{varname}\s*=\s*'''(.*?)'''", source, re.DOTALL)
        if m:
            svg_m = re.search(r'(<svg[\s\S]*?</svg>)', m.group(1))
            if svg_m:
                genesis_svgs[varname] = (svg_m.group(1), label)

    results = []

    if flat_svg:
        results.append({
            'key': 'inject_flat_diagram',
            'terms': ['(inline chapter injection)'],
            'svg': flat_svg,
            'title': 'Flat cross-section (inline chapter version)',
            'context_html': '<p><strong>inject_flat_diagram()</strong> — Flat cross-section injected after canonical definition in chapter. <span style="color:#1a8a6a;font-weight:bold;">ANIMATED</span> — electrons drift horizontally (Plan 0267).</p>',
            'source': 'preprocess.py',
        })

    if domain_svg:
        results.append({
            'key': 'inject_domain_buttons',
            'terms': ['(inline chapter injection)'],
            'svg': domain_svg,
            'title': '11-domain button network (full chapter version with legend)',
            'context_html': '<p><strong>inject_domain_buttons()</strong> — 11-domain button network with legend, injected after five-fields paragraph. <span style="color:#1a8a6a;font-weight:bold;">ANIMATED</span> — dashed bridges pulse (Plan 0267).</p>',
            'source': 'preprocess.py',
        })

    for varname, (svg, label) in genesis_svgs.items():
        animated = varname != 'CANOPY_PROBLEM'
        anim_note = ' <span style="color:#1a8a6a;font-weight:bold;">ANIMATED</span> (Plan 0267)' if animated else ''
        results.append({
            'key': f'genesis_{varname.lower()}',
            'terms': ['(inline chapter injection)'],
            'svg': svg,
            'title': f'{label} (Genesis inline)',
            'context_html': f'<p><strong>inject_genesis_illustrations()</strong> — {label}, injected inline in Genesis chapter.{anim_note}</p>',
            'source': 'preprocess.py',
        })

    for i, panel_svg in enumerate(filmstrip_panels, 1):
        labels = ['scatter', 'tie and toss', 'early clusters', 'growing net', 'almost', 'phase transition']
        results.append({
            'key': f'filmstrip-panel-{i}',
            'terms': ['(inline chapter injection)'],
            'svg': panel_svg,
            'title': f'Kauffman filmstrip panel {i}: {labels[i-1]}',
            'context_html': f'<p><strong>inject_button_sequence()</strong> — Panel {i}/6: {labels[i-1]}</p>',
            'source': 'preprocess.py',
        })

    return results



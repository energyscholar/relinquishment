def load_standalone_svgs():
    """Load standalone SVG files from build/images/."""
    results = []
    images_dir = REPO / 'build' / 'images'
    for svg_path in sorted(images_dir.glob('*.svg')):
        svg_content = svg_path.read_text()
        if '<?xml' in svg_content:
            svg_content = re.sub(r'<\?xml[^?]*\?>\s*', '', svg_content)
        results.append({
            'key': svg_path.stem,
            'terms': [f'build/images/{svg_path.name}'],
            'svg': svg_content.strip(),
            'title': svg_path.stem.replace('-', ' ').title(),
            'context_html': f'<p><strong>{svg_path.name}</strong> — standalone SVG file</p>',
            'source': 'build/images/',
        })
    return results


def load_planned_from_manifest():
    """Load planned (not-yet-built) entries from gallery-manifest.yaml."""
    manifest_path = REPO / 'build' / 'gallery-manifest.yaml'
    if not manifest_path.exists():
        return []
    entries = yaml.safe_load(manifest_path.read_text()) or []
    planned = []
    for e in entries:
        if not isinstance(e, dict):
            continue
        if e.get('status') != 'planned':
            continue
        planned.append(e)
    return planned


